# Compute a)5 to the power of 8 b)square root of 400 c)exponent of 5 d)Logarithm of 625
# base 5
import math

# a) 5 to the power of 8
power_result = 5 ** 8

# b) Square root of 400
sqrt_result = math.sqrt(400)

# c) Exponent of 5 (e^5)
exp_result = math.exp(5)

# d) Logarithm of 625 with base 5
log_result = math.log(625, 5)

print(f"a) 5 to the power of 8: {power_result}")
print(f"b) Square root of 400: {sqrt_result}")
print(f"c) Exponent of 5 (e^5): {exp_result}")
print(f"d) Logarithm of 625 with base 5: {log_result}")
